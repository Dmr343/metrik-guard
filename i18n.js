function setLang(lang) {
  const t = translations[lang] || translations['es'];

  /* Apply text translations */
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      el.textContent = t[key];
    }
  });

  /* Apply HTML translations (for elements with HTML tags like <br>, <span>) */
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) {
      el.innerHTML = t[key];
    }
  });

  /* Update lang attribute on <html> */
  document.documentElement.lang = lang;

  /* Store preference */
  localStorage.setItem('mg-lang', lang);

  /* Update lang toggle button text (shows the OTHER language) */
  const btn = document.getElementById('langToggle');
  if (btn) {
    btn.textContent = lang === 'es' ? 'EN' : 'ES';
  }
}

/* Auto-apply on page load */
(function() {
  const saved = localStorage.getItem('mg-lang') || 'es';
  /* Only run setLang if not Spanish (ES is the default markup language) */
  /* Always run to set button text correctly */
  document.addEventListener('DOMContentLoaded', function() {
    setLang(saved);
  });
})();
